#!/bin/zsh
# Собирает DMG для раздачи: приложение + ярлык на /Applications + инструкция.
set -euo pipefail
cd "$(dirname "$0")"

./build.sh

VERSION=$(/usr/libexec/PlistBuddy -c "Print CFBundleShortVersionString" Resources/Info.plist)
STAGE="build/dmg-stage"
DMG="build/Микшер громкости $VERSION.dmg"

rm -rf "$STAGE" "$DMG"
mkdir -p "$STAGE"
cp -R "build/Микшер громкости.app" "$STAGE/"
ln -s /Applications "$STAGE/Программы"

cat > "$STAGE/ПРОЧТИ МЕНЯ.txt" <<'EOF'
МИКШЕР ГРОМКОСТИ — громкость каждого приложения по отдельности

УСТАНОВКА
1. Перетащи «Микшер громкости» в папку «Программы».
2. Запусти его из «Программ».

macOS СКАЖЕТ, ЧТО РАЗРАБОТЧИК НЕ ПОДТВЕРЖДЁН — ЭТО НОРМАЛЬНО
Приложение собрано без платного аккаунта Apple, поэтому при первом
запуске система его заблокирует. Один раз сделай так:
1. Открой Системные настройки → Конфиденциальность и безопасность.
2. Прокрути вниз — там будет «"Микшер громкости" заблокировано…»
   и кнопка «Всё равно открыть». Нажми её и подтверди.
3. При первом запуске разреши «запись системного звука» — без этого
   регулировать громкость приложений технически невозможно.
   (Микшер ничего не записывает и никуда не отправляет: разрешение
   нужно, потому что перехват звука приложений в macOS проходит
   через тот же механизм.)

КАК ПОЛЬЗОВАТЬСЯ
Иконка с ползунками появится в строке меню (справа сверху, рядом
с часами). Клик — и вот твой микшер: громкость и mute для каждого
приложения, общая громкость снизу. Настройки запоминаются.

Требуется macOS 15 или новее.
EOF

hdiutil create -volname "Микшер громкости" -srcfolder "$STAGE" -ov -format UDZO "$DMG" >/dev/null
rm -rf "$STAGE"
echo "Готово: $DMG"
